package cn.baidu.test;

public class TestMath {

	public static void main(String[] args) {

		System.out.println(Math.abs(10));//取绝对值
		System.out.println(Math.abs(-10));//取绝对值
		
		System.out.println(Math.ceil(12.2));//向上取整
		
		System.out.println(Math.floor(12.5));//向下取整
		
		System.out.println(Math.max(14,13));//取最大值
		
		System.out.println(Math.min(23, 32));//取最小值
		
		System.out.println(Math.pow(12, 2));//乘方
		
		System.out.println(Math.random());//产生随机数
		
		System.out.println(Math.round(12.2));//四舍五入
		System.out.println(Math.round(12.5));
	}

}
